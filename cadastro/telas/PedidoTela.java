    private ArrayList<Itemcompra> pegarItensPedido() {
        String codigo = ""; 
        String qtd = "";
        String valorUnitario = "";
        String valorTotalItem = "";
        for (int i = 0; i < tbItensPedido.getRowCount(); i++) {
            Itemcompra item = new Itemcompra();
            //item.setIdItemPedido(i);
            item.setIdPedido(Integer.parseInt(txtIdPedido.getText()));
            
            //Pegando itens da tabela de Itens
            codigo = (String) tbItensPedido.getValueAt(i, 0);
            Produto p = new Produto();
            ProdutoDAO produtoDAO = new ProdutoDAO();
            p = produtoDAO.buscar(codigo);
            qtd = (String) tbItensPedido.getValueAt(i, 2);
            valorUnitario = (String) tbItensPedido.getValueAt(i, 3);
            valorTotalItem = (String) tbItensPedido.getValueAt(i, 4);
                    
            item.setIdProduto(p.getId());
            item.setQtd(Double.parseDouble(qtd));
            item.setValorUnitario(Double.parseDouble(valorUnitario));
            item.setValorTotalItem(Double.parseDouble(valorTotalItem));
            
            itensPedido.add(item);
        }
        
        return itensPedido;
    }
    
