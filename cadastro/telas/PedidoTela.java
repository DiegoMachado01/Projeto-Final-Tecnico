    private void tbItensPedidoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tbItensPedidoKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_DELETE){
            DefaultTableModel tabelaItem = (DefaultTableModel)tbItensPedido.getModel(); 
            int linha = tbItensPedido.getSelectedRow();
            double totalItem =  Double.parseDouble((String) tbItensPedido.getValueAt(linha, 4));
            calcularTotalPedido(totalItem * -1);
            tabelaItem.removeRow(linha);
        }
    }//GEN-LAST:event_tbItensPedidoKeyPressed

